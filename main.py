#Asking for inputs for base and height
base = float(input('Enter a value for the base of the triangle: \n'))
height = float(input('Enter a value for the height of the triangle: \n'))

#Getting area of the triangle

area = (base*height)/2

#Printing outputs

print(f'A triangle with a base of {base} units and height of {height} units has an area of {area} units squared')